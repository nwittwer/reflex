<template>
  <div id="main-view">
    <ToolBar ref="toolbar" />
    <div id="canvasContainer">
      <SidePanel />
      <Screenshots />
      <div id="canvas" ref="canvas">
        <Artboards ref="artboards" />
      </div>
    </div>
  </div>
</template>

<script>
import ToolBar from "../components/ToolBar";
import Artboards from "../components/Artboards.vue";
import SidePanel from "../components/SidePanel";
import Screenshots from "../components/Screenshot";
import store from "@/store";
import { Panzoom } from "../mixins/panzoom";
import { ipcRenderer } from "electron";

export default {
  name: "MainView",
  components: {
    Artboards,
    SidePanel,
    ToolBar,
    Screenshots
  },
  computed: {
    // Bind to our Vuex Store's URL value
    artboards: function() {
      return this.$store.state.artboards;
    }
  },
  mounted: function() {
    let vm = this;

    vm.$nextTick(() => {
      const contentEl = vm.$refs["artboards"].$el;
      const controllerEl = vm.$refs["canvas"];

      // Initialize Panzoom
      let instance = new Panzoom(contentEl, controllerEl, {
        startCentered: true
      });
      document.$panzoom = instance; // Attach to document

      // Listen for changes
      // Commit them to the Vuex store
      instance
        .on("zoomStart", () => {
          store.commit("interactionSetState", {
            key: "isZooming",
            value: true
          });
        })
        .on("zoomStop", () => {
          store.commit("interactionSetState", {
            key: "isZooming",
            value: false
          });
        })
        .on("panStart", () => {
          store.commit("interactionSetState", {
            key: "isPanning",
            value: true
          });
        })
        .on("panStop", () => {
          store.commit("interactionSetState", {
            key: "isPanning",
            value: false
          });
        });
    });

    // Listen for menu bar events
    // TODO Add tests for these
    ipcRenderer.on("menu_zoom-to-fit", () => {
       document.$panzoom.fitToScreen();
    });

    ipcRenderer.on("menu_zoom-in", () => {
       document.$panzoom.zoomIn()
    });

    ipcRenderer.on("menu_zoom-out", () => {
      document.$panzoom.zoomOut()
    });
  }
};
</script>

